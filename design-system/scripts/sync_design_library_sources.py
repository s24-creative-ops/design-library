def update_content_data() -> dict[str, Any]:
    content_data = load_assignment_json(
        CONTENT_DATA_PATH,
        [CONTENT_DATA_MARKER, LEGACY_CONTENT_DATA_MARKER],
    )
    if not isinstance(content_data, dict):
        fail("content-data.js enthaelt kein Objekt auf Top-Level.")

    if "sections" not in content_data or "email" not in content_data["sections"]:
        fail("content-data.js enthaelt keine erwartete sections.email-Struktur.")
    if "tokens" not in content_data:
        fail("content-data.js enthaelt kein erwartetes tokens-Feld.")

    canonical_tokens = json.loads(read_text(TOKENS_PATH))
    email_modules = build_email_modules_map()
    service_products = build_service_products()

    content_data["tokens"] = build_tokens_snapshot(canonical_tokens, content_data["tokens"])
    content_data["emailModules"] = email_modules
    content_data["serviceProducts"] = service_products
    content_data.pop("lpModuleBasePath", None)

    home_page = content_data.get("homePage")
    if isinstance(home_page, dict):
        home_page["label"] = "Design Library"
        home_page["intro"] = (
            "Die Library ist die zentrale Quelle fuer die GPT Builder. "
            "Sie buendelt Design Tokens, E-Mail-Builder-Module und LP-Builder-Module "
            "und dient als Referenz fuer konsistente Builder-Ausgaben."
        )
        home_page["quickLinks"] = [
            {
                "label": "E-Mail Builder",
                "section": "email",
                "text": "Module, Templates und Preview-Bausteine fuer den E-Mail-Bereich.",
                "gptLabel": "E-Mail Builder öffnen",
                "gptHref": "https://chatgpt.com/g/g-69c248087a7c819188ed0fe9f4ee2e14-e-mail-builder-beta",
            },
            {
                "label": "LP Builder",
                "section": "lp",
                "text": "Landingpage-Module aus den synchronisierten LP-Artefakten.",
                "gptLabel": "LP Builder öffnen",
                "gptHref": "https://chatgpt.com/g/g-69839550ac988191bd3a3838d2b00d02-landing-page-builder",
            },
        ]
        home_page.pop("note", None)
        home_page.pop("sections", None)

    lp_section = content_data.get("sections", {}).get("lp")
    if isinstance(lp_section, dict):
        lp_section["label"] = "LP Builder"

    email_items = content_data["sections"]["email"].get("items")
    if not isinstance(email_items, list):
        fail("content-data.js enthaelt kein gueltiges sections.email.items-Array.")

    for item in email_items:
        if not isinstance(item, dict) or "kind" not in item:
            fail("content-data.js enthaelt ein unerwartetes sections.email-Item.")

        item_id = item.get("id")
        if isinstance(item_id, str) and item_id in EMAIL_SECTION_LABELS:
            item["label"] = EMAIL_SECTION_LABELS[item_id]

        kind = item["kind"]
        if kind == "emailModules":
            item["libraryHtml"] = build_email_library_html(item, email_modules)
        elif kind == "serviceProducts":
            item.pop("note", None)
            item["libraryHtml"] = build_service_products_library_html(item, email_modules)
        else:
            fail(
                f"content-data.js enthaelt ein unbekanntes sections.email.kind '{kind}'. "
                "Teilgenerierung wird zur Sicherheit abgebrochen."
            )

    return content_data


def validate_design_library_compatibility(
    content_data: dict[str, Any], lp_markup: dict[str, str], shadow_css: str
) -> None:
    required_top_level_keys = ["homePage", "sections", "tokens", "serviceProducts"]
    for key in required_top_level_keys:
        if key not in content_data:
            fail(f"Die generierte content-data.js fehlt am Top-Level das erwartete Feld '{key}'.")

    if not isinstance(content_data["sections"].get("email", {}).get("items"), list):
        fail("Die generierte content-data.js fehlt sections.email.items.")
    if not isinstance(content_data["sections"].get("lp", {}).get("items"), list):
        fail("Die generierte content-data.js fehlt sections.lp.items.")
    if not isinstance(content_data.get("serviceProducts"), list):
        fail("Die generierte content-data.js fehlt ein gueltiges serviceProducts-Array.")
    if not isinstance(lp_markup, dict) or not lp_markup:
        fail("lp-module-markup.js wurde leer generiert.")
    if not shadow_css.strip():
        fail("lp-shadow-styles.js wurde leer generiert.")


def generate_outputs() -> dict[Path, str]:
    content_data = update_content_data()
    lp_metadata = load_lp_module_metadata_via_lp_sync()
    content_data["sections"]["lp"] = build_lp_content_section_via_lp_sync(
        lp_metadata,
        content_data.get("sections", {}).get("lp"),
    )
    lp_markup = extract_lp_module_markup_via_lp_sync([module["id"] for module in lp_metadata])
    lp_shadow_css = build_lp_shadow_styles_via_lp_sync()

    validate_design_library_compatibility(content_data, lp_markup, lp_shadow_css)

    return {
        CONTENT_DATA_PATH: render_assignment(CONTENT_DATA_MARKER, content_data),
        LP_MARKUP_PATH: render_lp_markup_assignment_via_lp_sync(lp_markup),
        LP_SHADOW_STYLES_PATH: render_lp_shadow_assignment_via_lp_sync(lp_shadow_css),
    }


def sync() -> None:
    rendered_outputs = generate_outputs()
    for path, content in rendered_outputs.items():
        write_text(path, content)

    print("Design-Library-Artefakte synchronisiert.")
    print(f"- {CONTENT_DATA_PATH.relative_to(PROJECT_ROOT)}")
    print(f"- {LP_MARKUP_PATH.relative_to(PROJECT_ROOT)}")
    print(f"- {LP_SHADOW_STYLES_PATH.relative_to(PROJECT_ROOT)}")


def check() -> None:
    rendered_outputs = generate_outputs()
    drifted_paths: list[Path] = []

    for path, expected_content in rendered_outputs.items():
        current_content = read_text(path)
        if current_content != expected_content:
            drifted_paths.append(path)

    if drifted_paths:
        print("Design-Library-Artefakte sind nicht aktuell.", file=sys.stderr)
        for path in drifted_paths:
            print(f"- {path.relative_to(PROJECT_ROOT)}", file=sys.stderr)
        raise SystemExit(1)

    print("Design-Library-Artefakte sind aktuell.")


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Synchronisiert oder prueft die generierten Artefakte der offiziellen design-system/design-library/."
    )
    parser.add_argument(
        "--check",
        action="store_true",
        help="Vergleicht die generierten Inhalte nur im Speicher und schreibt keine Dateien.",
    )
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    try:
        if args.check:
            check()
        else:
            sync()
    except SyncError as exc:
        print(f"Fehler: {exc}", file=sys.stderr)
        return 1
    except SystemExit as exc:
        code = exc.code if isinstance(exc.code, int) else 1
        return code
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
